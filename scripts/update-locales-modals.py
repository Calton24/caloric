for lang, d in DIRS.items():
    path = os.path.join(d, "common.json")
    with open(path, "r", encoding="utf-8") as f:
        data = json.load(f)
    src = EN_KEYS if lang == "en" else TRANSLATIONS.get(lang, EN_KEYS)
    added = {}
    for section, section_keys in src.items():
        if section not in data:
            data[section] = {}
        count = 0
        for k, v in section_keys.items():
            if k not in data[section]:
                data[section][k] = v
                count += 1
        if count:
            added[section] = count
    with open(path, "w", encoding="utf-8") as f:
        json.dump(data, f, indent=2, ensure_ascii=False)
        f.write("\n")
    print(f"{lang}: {added}")
