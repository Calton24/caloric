/**
 * Look up a food name in the ontology.
 * Tries: exact → synonym → multi-word keys → single-word partial → fuzzy.
 */
export function lookupOntology(foodName: string): OntologyEntry | null {
  const normalized = foodName.toLowerCase().trim();

  // 1. Exact match
  if (FOOD_ONTOLOGY[normalized]) {
    return FOOD_ONTOLOGY[normalized];
  }

  // 2. Synonym / alias lookup
  const synonymTarget = SYNONYMS[normalized];
  if (synonymTarget && FOOD_ONTOLOGY[synonymTarget]) {
    return FOOD_ONTOLOGY[synonymTarget];
  }

  // 3. Multi-word synonym check (e.g., "cup of joe" in "a cup of joe")
  const multiWordSynonyms = Object.keys(SYNONYMS)
    .filter((k) => k.includes(" "))
    .sort((a, b) => b.length - a.length);

  for (const key of multiWordSynonyms) {
    if (normalized.includes(key)) {
      const target = SYNONYMS[key];
      if (FOOD_ONTOLOGY[target]) {
        return FOOD_ONTOLOGY[target];
      }
    }
  }

  // 4. Multi-word ontology keys that appear in the input
  const multiWordKeys = Object.keys(FOOD_ONTOLOGY)
    .filter((k) => k.includes(" "))
    .sort((a, b) => b.length - a.length);

  for (const key of multiWordKeys) {
    if (normalized.includes(key)) {
      return FOOD_ONTOLOGY[key];
    }
  }

  // 5 & 6. Single-word key/synonym match — prefer the LONGEST match.
  //         "honey cornflakes" should match "cornflakes" (8 chars) not "honey" (5 chars).
  //         We scan BOTH ontology keys AND synonyms in one pass and pick the longest hit.
  let bestEntry: OntologyEntry | null = null;
  let bestLength = 0;

  for (const key of Object.keys(FOOD_ONTOLOGY)) {
    if (
      (normalized === key || normalized.includes(key)) &&
      key.length > bestLength
    ) {
      bestEntry = FOOD_ONTOLOGY[key];
      bestLength = key.length;
    }
  }

  for (const synKey of Object.keys(SYNONYMS)) {
    if (
      (normalized === synKey || normalized.includes(synKey)) &&
      synKey.length > bestLength
    ) {
      const target = SYNONYMS[synKey];
      if (FOOD_ONTOLOGY[target]) {
        bestEntry = FOOD_ONTOLOGY[target]; // matched via synKey
        bestLength = synKey.length;
      }
    }
  }

  if (bestEntry) {
    return bestEntry;
  }

  // 7. Fuzzy match: handle plurals, typos, -ing/-ed suffixes
  const stripped = normalized
    .replace(/s$/, "") // spliffs → spliff
    .replace(/es$/, "") // watches → watch
    .replace(/ing$/, "") // vaping → vap(e)
    .replace(/ed$/, ""); // smoked → smok(e)

  if (stripped !== normalized) {
    // Try stripped form against synonyms first, then ontology
    if (SYNONYMS[stripped] && FOOD_ONTOLOGY[SYNONYMS[stripped]]) {
      return FOOD_ONTOLOGY[SYNONYMS[stripped]];
    }
    if (FOOD_ONTOLOGY[stripped]) {
      return FOOD_ONTOLOGY[stripped];
    }
    // Try adding 'e' back (vap → vape, smok → smoke)
    const withE = stripped + "e";
    if (SYNONYMS[withE] && FOOD_ONTOLOGY[SYNONYMS[withE]]) {
      return FOOD_ONTOLOGY[SYNONYMS[withE]];
    }
    if (FOOD_ONTOLOGY[withE]) {
      return FOOD_ONTOLOGY[withE];
    }
  }

  return null;
}

/**
 * Detect modifiers in the raw input that the ontology entry cares about.
 * Returns the list of detected modifier keywords.
 */
export function detectModifiers(
  rawInput: string,
  entry: OntologyEntry
): string[] {
  const normalized = rawInput.toLowerCase();
  return entry.modifierKeywords.filter((kw) => normalized.includes(kw));
}

/**
 * Check if the input looks like a branded product.
 * Looks for brand indicators, store names, and proper nouns.
 */
export function detectBrandedIntent(rawInput: string): boolean {
  const normalized = rawInput.toLowerCase();

  const brandSignals = [
    // Store names
    /\b(starbucks|mcdonald'?s|subway|dunkin|chipotle|wendys|burger king|taco bell|chick-fil-a|panda express)\b/,
    /\b(lidl|aldi|trader joe'?s|whole foods|costco|walmart|target|kroger|tesco|sainsbury'?s|waitrose)\b/,
    // Brand pattern: "X brand Y" or "Y from X"
    /\bfrom\s+\w+/,
    /\bbrand\b/,
    // Common branded food hints
    /\b(oreo|nutella|coca.?cola|pepsi|sprite|fanta|gatorade|powerade|monster|red bull|celsius)\b/,
    /\b(cliff bar|kind bar|rxbar|quest|fairlife|chobani|fage|siggi|oikos|yoplait|dannon)\b/,
    // Packaging indicators
    /\b(packaged|branded|store.?bought|pre-made|frozen meal|ready meal)\b/,
  ];

  return brandSignals.some((re) => re.test(normalized));
}

/**
 * Build a default assumption label for the UI.
 * e.g., "Assuming black coffee: 2 kcal"
 */
export function buildAssumptionLabel(
  entry: OntologyEntry,
  detectedModifiers: string[]
): string | undefined {
  if (detectedModifiers.length > 0) {
    // Modifiers present — ontology defaults don't apply
    return undefined;
  }
  if (!entry.clarifyIfModifiersMissing) {
    return undefined;
  }
  return `Assuming ${entry.defaultAssumption}`;
}
