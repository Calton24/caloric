/**
 * Food Ontology
 *
 * Deterministic category map for foods. This is the "adult supervision" layer
 * that runs BEFORE any AI estimation or database lookup.
 *
 * Knows:
 *   - "coffee" without modifiers = black coffee ≈ 2 kcal
 *   - "latte" = milk-based coffee ≈ 190 kcal
 *   - "cigarette" = not food = 0 kcal
 *   - "water" = zero-cal beverage
 *
 * The ontology resolves the canonical identity, default calories, category,
 * and whether clarification is needed BEFORE hitting any database.
 */

// ─── Types ───────────────────────────────────────────────────────────────────

export type FoodCategory =
  | "beverage"
  | "protein"
  | "grain"
  | "fruit"
  | "vegetable"
  | "dairy"
  | "snack"
  | "meal"
  | "condiment"
  | "supplement"
  | "non-food"
  | "unknown";

export interface OntologyEntry {
  /** What category this food belongs to */
  category: FoodCategory;
  /** What to assume if no modifiers present */
  defaultAssumption: string;
  /** Default calories for this assumption (per serving) */
  defaultCalories: number;
  /** Default macros */
  defaultProtein: number;
  defaultCarbs: number;
  defaultFat: number;
  /** Should we ask the user to clarify? */
  clarifyIfModifiersMissing: boolean;
  /** Keywords that change the calorie profile significantly */
  modifierKeywords: string[];
  /** If branded product terms are detected, prefer OFF */
  preferOffIfBranded: boolean;
  /** Default serving description */
  servingDescription: string;
}

